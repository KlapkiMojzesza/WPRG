<?php
function dodaj($skladnik1, $skladnik2) {
  echo $skladnik1 + $skladnik2;
}
function odejmij($skladnik1, $skladnik2) {
  echo $skladnik1 - $skladnik2;
}
function pomnoz($skladnik1, $skladnik2) {
  echo $skladnik1 * $skladnik2;
}
function podziel($skladnik1, $skladnik2) {
  echo $skladnik1 / $skladnik2;
}
